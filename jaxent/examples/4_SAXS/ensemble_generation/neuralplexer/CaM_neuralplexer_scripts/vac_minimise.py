#!/usr/bin/env python3
"""
minimise_ensemble.py

In-vacuo constrained minimisation of NeuralPLexer protein–ligand complex
ensembles produced by combine_outputs.py.

Workflow per frame
------------------
  1. Parse combined complex PDB (complex_rank*.pdb).
  2. Detect Ca²⁺/Ca²⁺ clashes; for each clashing pair remove the ion with
     fewer oxygen contacts within ``--ca-o-cutoff`` Å.  Pairs are resolved
     greedily in ascending distance order.
  3. Build a hybrid OpenMM force field:
       Protein  → AMBER ff14SB  (PDBFixer adds missing atoms + Hs)
       Ca²⁺     → AMBER Ca²⁺ parameters  (bundled in amber14-all.xml)
       CDZ      → OpenFF Sage 2.1.0
  4. Harmonic positional restraints on protein Cα atoms + Ca²⁺ ions
     (default k = 1000 kJ/mol/nm²).  CDZ ligands are completely unrestrained.
  5. In-vacuo L-BFGS minimisation (NoCutoff; prefers CUDA → OpenCL → CPU).
  6. Write individual PDB files with CONECT records for CDZ bonds.
     Filename encodes final Ca²⁺ and CDZ counts:
       complex_rank{R}_plddt{P}_ca{N}_cdz{M}_minimised.pdb

Requirements
------------
    openmm openmmforcefields 'openff-toolkit>=0.14' openff-units
    pdbfixer rdkit MDAnalysis numpy

Usage
-----
    python minimise_ensemble.py \\
        --input-dir  combined_outputs/ \\
        --ligand-sdf 85H_ideal.sdf    \\
        [--output-dir minimised/]     \\
        [--ca-clash-threshold 3.5]    \\
        [--ca-o-cutoff 3.0]           \\
        [--restraint-k 1000.0]        \\
        [--max-iterations 2000]       \\
        [--top-n 5]

Notes
-----
* The heavy-atom ordering in the combined PDB is assumed to match the
  85H_ideal.sdf template (guaranteed when combine_outputs.py is used
  without post-processing).
* H atoms for CDZ are generated by RDKit from the heavy-atom geometry.
* H atoms for the protein are added by PDBFixer at pH 7.0.
"""


def main() -> None:
    global OPENFF_FF
    parser = argparse.ArgumentParser(
        description=__doc__,
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
    parser.add_argument(
        "--input-dir",
        required=True,
        help="Directory containing complex_rank*.pdb files from combine_outputs.py.",
    )
    parser.add_argument(
        "--ligand-sdf",
        required=True,
        help="Path to 85H_ideal.sdf (CDZ template; provides atom connectivity).",
    )
    parser.add_argument(
        "--output-dir",
        default=None,
        help="Output directory (default: <input-dir>/minimised/).",
    )
    parser.add_argument(
        "--ca-clash-threshold",
        type=float,
        default=CA_CA_CLASH_THRESHOLD,
        help=f"Ca²⁺/Ca²⁺ clash cutoff in Å (default {CA_CA_CLASH_THRESHOLD}).",
    )
    parser.add_argument(
        "--ca-o-cutoff",
        type=float,
        default=CA_O_CONTACT_CUTOFF,
        help=f"Oxygen-contact cutoff for Ca²⁺ quality scoring in Å "
        f"(default {CA_O_CONTACT_CUTOFF}).",
    )
    parser.add_argument(
        "--restraint-k",
        type=float,
        default=DEFAULT_RESTRAINT_K,
        help=f"Force constant for Cα / Ca²⁺ positional restraints "
        f"in kJ/mol/nm² (default {DEFAULT_RESTRAINT_K}).",
    )
    parser.add_argument(
        "--max-iterations",
        type=int,
        default=DEFAULT_MAX_ITER,
        help=f"Maximum L-BFGS minimisation steps (default {DEFAULT_MAX_ITER}).",
    )
    parser.add_argument(
        "--top-n",
        type=int,
        default=None,
        help="Process only the top N frames ranked by pLDDT rank index.",
    )
    parser.add_argument(
        "--openff-ff",
        default=OPENFF_FF,
        help=f"OpenFF force field string (default '{OPENFF_FF}').",
    )
    args = parser.parse_args()

    # Override global constant if user supplied a different FF
    OPENFF_FF = args.openff_ff

    input_dir = Path(args.input_dir).resolve()
    output_dir = Path(args.output_dir).resolve() if args.output_dir else input_dir / "minimised"
    ligand_sdf = Path(args.ligand_sdf).resolve()

    if not input_dir.is_dir():
        sys.exit(f"[ERROR] --input-dir not found: {input_dir}")
    if not ligand_sdf.exists():
        sys.exit(f"[ERROR] --ligand-sdf not found: {ligand_sdf}")

    output_dir.mkdir(parents=True, exist_ok=True)

    # Discover and sort frames by rank
    def _rank_key(p: Path) -> int:
        result = parse_rank_plddt(p.name)
        return result[0] if result is not None else 0

    pdb_files = sorted(
        (p for p in input_dir.glob("complex_rank*.pdb") if parse_rank_plddt(p.name)),
        key=_rank_key,
    )
    if not pdb_files:
        sys.exit(f"[ERROR] No complex_rank*.pdb files found in {input_dir}")

    if args.top_n is not None:
        pdb_files = pdb_files[: args.top_n]

    # ── Banner ────────────────────────────────────────────────────────────
    print(f"\n{'═' * 74}")
    print("  NeuralPLexer Ensemble In-Vacuo Constrained Minimisation")
    print(f"{'═' * 74}")
    print(f"  Frames to process  : {len(pdb_files)}")
    print(f"  Input dir          : {input_dir}")
    print(f"  Output dir         : {output_dir}")
    print(f"  CDZ template       : {ligand_sdf}")
    print(f"  Ca clash threshold : {args.ca_clash_threshold} Å")
    print(f"  Ca O-contact cutoff: {args.ca_o_cutoff} Å")
    print(f"  Restraint k (Cα/Ca): {args.restraint_k} kJ/mol/nm²")
    print(f"  Max iterations     : {args.max_iterations}")
    print(f"  OpenFF FF          : {OPENFF_FF}")
    print(f"{'═' * 74}")

    # ── Pre-build CDZ template + SystemGenerator (cached across frames) ──────
    print("  Pre-building CDZ ligand cache (AM1-BCC charges) ...")
    cached_template_rdkit = load_cdz_template_rdkit(ligand_sdf)
    _proto_h = Chem.AddHs(cached_template_rdkit, addCoords=True)
    cdz_prototype = OFFMolecule.from_rdkit(_proto_h, allow_undefined_stereo=True)

    _ca_xml_fd, _ca_xml_path = tempfile.mkstemp(suffix=".xml")
    try:
        os.write(_ca_xml_fd, _CA_ION_XML.encode())
        os.close(_ca_xml_fd)
        cached_system_generator = SystemGenerator(
            forcefields=["amber14-all.xml", _ca_xml_path],
            small_molecule_forcefield=OPENFF_FF,
            molecules=[cdz_prototype],
            forcefield_kwargs={"constraints": None, "removeCMMotion": False},
            nonperiodic_forcefield_kwargs={"nonbondedMethod": app.NoCutoff},
            cache=None,
        )
    finally:
        os.unlink(_ca_xml_path)
    print("  CDZ cache ready.")

    n_ok = 0
    for pdb_path in pdb_files:
        try:
            ok = process_frame(
                pdb_path,
                ligand_sdf,
                output_dir,
                ca_clash_threshold=args.ca_clash_threshold,
                ca_o_cutoff=args.ca_o_cutoff,
                restraint_k=args.restraint_k,
                max_iterations=args.max_iterations,
                cached_template_rdkit=cached_template_rdkit,
                cached_system_generator=cached_system_generator,
            )
            if ok:
                n_ok += 1
        except Exception as exc:
            print(f"\n  [ERROR] Unhandled exception for {pdb_path.name}: {exc}")
            traceback.print_exc()

    print(f"\n{'═' * 74}")
    print(f"  Finished: {n_ok}/{len(pdb_files)} frames minimised  →  {output_dir}")
    print(f"{'═' * 74}\n")


if __name__ == "__main__":
    main()
